	scrollViewportPages(direction: -1 | 1): boolean {
		const height = this.terminal.rows;
		const width = this.terminal.columns;
		if (height <= 0 || width <= 0) return false;
		const viewport = this.#viewportSource();
		if (viewport !== undefined) {
			const { source, index } = viewport;
			const prefixRows = this.children
				.slice(0, index)
				.reduce((count, child) => count + safeRenderComponent(child, width, "viewport-prefix").length, 0);
			const tailRows = this.children
				.slice(index + 1)
				.reduce((count, child) => count + safeRenderComponent(child, width, "viewport-tail").length, 0);
			const maxViewportTop = Math.max(0, prefixRows + source.getLogicalRowCount(width) + tailRows - height);
			const resolved = this.#resolveBoundedViewportAnchor(source, prefixRows, width);
			const currentViewportTop = Math.max(
				0,
				Math.min(maxViewportTop, resolved ?? this.#manualViewportTop ?? this.#viewportTopRow),
			);
			const targetViewportTop = Math.max(
				0,
				Math.min(maxViewportTop, currentViewportTop + direction * Math.max(1, height - 1)),
			);
			if (!this.#captureBoundedViewportAnchor(source, width, height, prefixRows, targetViewportTop, direction)) {
				return this.#manualViewportAnchor === null;
			}
			this.#manualViewportTop = targetViewportTop;
			this.#doRender();
			return true;
		}
		if (this.#previousLines.length === 0) return false;
		const maxViewportTop = Math.max(0, this.#previousLines.length - height);
		const currentViewportTop = Math.max(0, Math.min(maxViewportTop, this.#manualViewportTop ?? this.#viewportTopRow));
		const targetViewportTop = Math.max(
			0,
			Math.min(maxViewportTop, currentViewportTop + direction * Math.max(1, height - 1)),
		);
		this.#manualViewportTop = targetViewportTop;
		return this.#repaintViewportFromLines(
			this.#previousLines,
			width,
			height,
			targetViewportTop,
			null,
			"manual viewport scroll",
		);
	}

	followLiveViewport(): boolean {
		if (this.#manualViewportTop === undefined) return false;
		const source = this.#viewportSource();
		if (source !== undefined) {
			this.#manualViewportTop = undefined;
			this.#doRender();
			return true;
		}
		const height = this.terminal.rows;
		const width = this.terminal.columns;
		const liveLines = this.#latestRenderedLines;
		const liveViewportTop = Math.max(0, liveLines.length - height);
		this.#manualViewportTop = undefined;
		this.#manualViewportAnchor = null;
		this.#manualViewportFallbackAnchors = [];
		this.#reconcileMissingViewportAnchor = false;
		const repainted = this.#repaintViewportFromLines(
			liveLines,
			width,
			height,
			liveViewportTop,
			this.#lastCursorPosition,
			"manual viewport follow live",
		);
		if (repainted) this.#previousLines = liveLines;
		return repainted;
	}

	/**
	 * Show an overlay component with configurable positioning and sizing.
	 * Returns a handle to control the overlay's visibility.
	 */
	showOverlay(component: Component, options?: OverlayOptions): OverlayHandle {
		const entry = { component, options, preFocus: this.#focusedComponent, hidden: false };
		this.overlayStack.push(entry);
		// Only focus if overlay is actually visible
		if (this.#isOverlayVisible(entry)) {
			this.setFocus(component);
		}
		this.#hideCursor();
		this.requestRender();

		// Return handle for controlling this overlay
		return {
			hide: () => {
				const index = this.overlayStack.indexOf(entry);
				if (index !== -1) {
					this.overlayStack.splice(index, 1);
					// Restore focus if this overlay had focus
					if (this.#focusedComponent === component) {
						const topVisible = this.#getTopmostVisibleOverlay();
						this.setFocus(topVisible?.component ?? entry.preFocus);
					}
					if (this.overlayStack.length === 0) this.#hideCursor();
					this.requestRender();
				}
			},
			setHidden: (hidden: boolean) => {
				if (entry.hidden === hidden) return;
				entry.hidden = hidden;
				// Update focus when hiding/showing
				if (hidden) {
					// If this overlay had focus, move focus to next visible or preFocus
					if (this.#focusedComponent === component) {
						const topVisible = this.#getTopmostVisibleOverlay();
						this.setFocus(topVisible?.component ?? entry.preFocus);
					}
				} else {
					// Restore focus to this overlay when showing (if it's actually visible)
					if (this.#isOverlayVisible(entry)) {
						this.setFocus(component);
					}
				}
				this.requestRender();
			},
			isHidden: () => entry.hidden,
		};
	}

	/** Hide the topmost overlay and restore previous focus. */
	hideOverlay(): void {
		const overlay = this.overlayStack.pop();
		if (!overlay) return;
		// Find topmost visible overlay, or fall back to preFocus
		const topVisible = this.#getTopmostVisibleOverlay();
		this.setFocus(topVisible?.component ?? overlay.preFocus);
		if (this.overlayStack.length === 0) this.#hideCursor();
		this.requestRender();
	}

	/** Check if there are any visible overlays */
	hasOverlay(): boolean {
		return this.overlayStack.some(o => this.#isOverlayVisible(o));
	}

	/** Check if an overlay entry is currently visible */
	#isOverlayVisible(entry: (typeof this.overlayStack)[number]): boolean {
		if (entry.hidden) return false;
		if (entry.options?.visible) {
			return entry.options.visible(this.terminal.columns, this.terminal.rows);
		}
		return true;
	}

	/** Find the topmost visible overlay, if any */
	#getTopmostVisibleOverlay(): (typeof this.overlayStack)[number] | undefined {
		for (let i = this.overlayStack.length - 1; i >= 0; i--) {
			if (this.#isOverlayVisible(this.overlayStack[i])) {
				return this.overlayStack[i];
			}
		}
		return undefined;
	}

	override invalidate(): void {
		super.invalidate();
		for (const overlay of this.overlayStack) overlay.component.invalidate?.();
	}

