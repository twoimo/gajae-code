type ViewportSourceRange = {
	component: Component;
	localStart: number;
	localEnd: number;
	direct: boolean;
};

/**
 * Container - a component that contains other components
 */
export class Container implements ViewportRowComponent {
	children: Component[] = [];
	#disposed = false;
	#viewportRowSources = new Map<Component, ViewportRowSource>();
	#viewportSourceRanges = new Map<string, ViewportSourceRange>();
	#viewportSourceIndexWidth = -1;
	#viewportSourceIndexChildren: Component[] = [];

	addChild(component: Component): void {
		this.children.push(component);
		this.#invalidateViewportSourceIndex();
	}

	/** Register extension-owned render-only content with a bounded viewport estimate. */
	addCappedChild(component: Component, maxRows: number): ViewportRowComponent {
		const capped = capViewportRows(component, maxRows);
		this.addChild(capped);
		return capped;
	}

	removeChild(component: Component): void {
		const index = this.children.indexOf(component);
		if (index !== -1) {
			this.children.splice(index, 1);
			this.#viewportRowSources.delete(component);
			component.dispose?.();
			this.#invalidateViewportSourceIndex();
		}
	}

	/** Remove a child without disposing it (for detach-then-readd reuse). */
	detachChild(component: Component): void {
		const index = this.children.indexOf(component);
		if (index !== -1) {
			this.children.splice(index, 1);
			this.#viewportRowSources.delete(component);
			this.#invalidateViewportSourceIndex();
		}
	}

	clear(): void {
		for (const child of this.children) child.dispose?.();
		this.children = [];
		this.#viewportRowSources.clear();
		this.#invalidateViewportSourceIndex();
	}

	/** Remove all children without disposing them (for detach-then-readd reuse). */
	detachAll(): void {
		this.children = [];
		this.#viewportRowSources.clear();
		this.#invalidateViewportSourceIndex();
	}

	/** Associates a direct row-addressable child with its durable semantic source. */
	setViewportRowSource(component: Component, source: ViewportRowSource | null): void {
		if (source === null) this.#viewportRowSources.delete(component);
		else if (!isViewportRowComponent(component))
			throw new TypeError("Viewport row sources require row-addressable components");
		else this.#viewportRowSources.set(component, source);
		this.#invalidateViewportSourceIndex();
	}

	resolveViewportAnchor(sourceId: string, graphemeIndex: number, width: number): number | undefined {
		width = Math.max(1, width);
		this.#ensureViewportSourceIndex(width);
		const range = this.#viewportSourceRanges.get(sourceId);
		if (!range || !isViewportRowComponent(range.component)) return undefined;
		const count = range.localEnd - range.localStart;
		const local = range.direct
			? this.#resolveChildViewportAnchor(range.component, graphemeIndex, width, count)
			: range.component.resolveViewportAnchor?.(sourceId, graphemeIndex, width);
		if (local === undefined || local < 0 || local >= count) return undefined;
		return range.localStart + local;
	}

	#ensureViewportSourceIndex(width: number): void {
		if (
			this.#viewportSourceIndexWidth === width &&
			this.#viewportSourceIndexChildren.length === this.children.length &&
			this.#viewportSourceIndexChildren.every((child, index) => child === this.children[index])
		) {
			return;
		}
		this.#viewportSourceRanges.clear();
		let offset = 0;
		for (const child of this.children) {
			const count = isViewportRowComponent(child)
				? child.getLogicalRowCount(width)
				: safeRenderComponent(child, width, "container-child").length;
			const localStart = offset;
			const localEnd = localStart + count;
			const source = this.#viewportRowSources.get(child);
			if (source) {
				this.#viewportSourceRanges.set(source.id, { component: child, localStart, localEnd, direct: true });
			} else if (isViewportRowComponent(child)) {
				const sourceIds = child.getViewportSourceIds?.() ?? [];
				for (let index = 0; index < sourceIds.length; index++) {
					this.#viewportSourceRanges.set(sourceIds[index]!, {
						component: child,
						localStart,
						localEnd,
						direct: false,
					});
				}
			}
			offset = localEnd;
		}
		this.#viewportSourceIndexWidth = width;
		this.#viewportSourceIndexChildren = this.children.slice();
	}

	#invalidateViewportSourceIndex(): void {
		this.#viewportSourceRanges.clear();
		this.#viewportSourceIndexWidth = -1;
		this.#viewportSourceIndexChildren = [];
	}

	#resolveChildViewportAnchor(
		child: ViewportRowComponent,
		graphemeIndex: number,
		width: number,
		count: number,
	): number | undefined {
		const window = (start: number, end: number): ViewportRowWindow =>
			child.renderRowsWithMetadata?.(width, start, end) ?? {
				lines: child.renderRows(width, start, end),
				metadata: Array<ViewportRowMetadata | null>(Math.max(0, end - start)).fill(null),
			};
		let low = 0;
		let high = count - 1;
		while (low <= high) {
			const middle = low + Math.floor((high - low) / 2);
			const probeStart = Math.max(0, middle - 8);
			const probe = window(probeStart, Math.min(count, middle + 9));
			let candidate: { row: number; graphemeStart: number; graphemeEnd: number } | undefined;
			for (let local = 0; local < probe.metadata.length; local++) {
				const metadata = probe.metadata[local];
				const graphemeStart = metadata?.graphemeStart;
				const graphemeEnd = metadata?.graphemeEnd;
				if (graphemeStart === undefined || graphemeEnd === undefined) continue;
				const row = probeStart + local;
				if (candidate === undefined || Math.abs(row - middle) < Math.abs(candidate.row - middle)) {
					candidate = { row, graphemeStart, graphemeEnd };
				}
			}
			if (!candidate) break;
			if (graphemeIndex < candidate.graphemeStart) high = candidate.row - 1;
			else if (graphemeIndex >= candidate.graphemeEnd) low = candidate.row + 1;
			else return candidate.row;
		}
		const from = Math.max(0, low - 8);
		const rendered = window(from, Math.min(count, Math.max(from + 1, low + 9)));
		for (let local = 0; local < rendered.metadata.length; local++) {
			const metadata = rendered.metadata[local];
			if (
				metadata?.graphemeStart !== undefined &&
				metadata.graphemeEnd !== undefined &&
				metadata.graphemeStart <= graphemeIndex &&
				graphemeIndex < metadata.graphemeEnd
			) {
				return from + local;
			}
		}
		return undefined;
	}

	dispose(): void {
		if (this.#disposed) return;
		this.#disposed = true;
		for (const child of this.children) child.dispose?.();
		this.#viewportRowSources.clear();
		this.#invalidateViewportSourceIndex();
	}

	invalidate(): void {
		for (const child of this.children) child.invalidate?.();
	}

	getLogicalRowCount(width: number): number {
		width = Math.max(1, width);
		let rows = 0;
		for (const child of this.children) {
			rows += isViewportRowComponent(child)
				? child.getLogicalRowCount(width)
				: safeRenderComponent(child, width, "container-child").length;
		}
		return rows;
	}

	renderRows(width: number, start: number, end: number): string[] {
		width = Math.max(1, width);
		const lines: string[] = [];
		let offset = 0;
		for (const child of this.children) {
			const count = isViewportRowComponent(child)
				? child.getLogicalRowCount(width)
				: safeRenderComponent(child, width, "container-child").length;
			const from = Math.max(0, start - offset);
			const to = Math.min(count, end - offset);
			if (from < to) {
				const rows = isViewportRowComponent(child)
					? child.renderRows(width, from, to)
					: safeRenderComponent(child, width, "container-child").slice(from, to);
				if (isViewportRowComponent(child)) recordFrameAllocationRowArray(rows, "container-viewport-child-return");
				else recordFrameAllocationRowArray(rows, "container-render-child-slice");
				for (let row = 0; row < rows.length; row++) lines.push(rows[row]!);
			}
			offset += count;
			if (offset >= end) break;
		}
		recordFrameAllocationRowArray(lines, "container-output");
		return lines;
	}

	renderRowsWithMetadata(width: number, start: number, end: number): ViewportRowWindow {
		width = Math.max(1, width);
		const lines: string[] = [];
		const metadata: Array<ViewportRowMetadata | null> = [];
		let offset = 0;
		for (const child of this.children) {
			const count = isViewportRowComponent(child)
				? child.getLogicalRowCount(width)
				: safeRenderComponent(child, width, "container-child").length;
			const from = Math.max(0, start - offset);
			const to = Math.min(count, end - offset);
			if (from < to) {
				const rendered = isViewportRowComponent(child)
					? (child.renderRowsWithMetadata?.(width, from, to) ?? {
							lines: child.renderRows(width, from, to),
							metadata: Array<ViewportRowMetadata | null>(to - from).fill(null),
						})
					: (() => {
							const slice = safeRenderComponent(child, width, "container-child").slice(from, to);
							return { lines: slice, metadata: Array<ViewportRowMetadata | null>(slice.length).fill(null) };
						})();
				if (rendered.lines.length !== rendered.metadata.length) {
					throw new Error(
						`Viewport row component returned ${rendered.metadata.length} metadata rows for ${rendered.lines.length} lines`,
					);
				}
				if (isViewportRowComponent(child))
					recordFrameAllocationRowArray(rendered.lines, "container-viewport-child-return");
				const source = this.#viewportRowSources.get(child);
				for (let row = 0; row < rendered.lines.length; row++) {
					lines.push(rendered.lines[row]);
					const item = rendered.metadata[row];
					metadata.push(source && item ? { ...item, sourceId: source.id } : item);
				}
			}
			offset += count;
			if (offset >= end) break;
		}
		recordFrameAllocationRowArray(lines, "container-output");
		return { lines, metadata };
	}

	getViewportSourceIds(): readonly string[] {
		const ids: string[] = [];
		for (const child of this.children) {
			const source = this.#viewportRowSources.get(child);
			if (source) {
				ids.push(source.id);
			} else if (isViewportRowComponent(child)) {
				const childIds = child.getViewportSourceIds?.() ?? [];
				for (let index = 0; index < childIds.length; index++) ids.push(childIds[index]!);
			}
		}
		return ids;
	}

	reconcileLogicalRowCount(width: number): number {
		for (const child of this.children) {
			if (isViewportRowComponent(child)) child.reconcileLogicalRowCount?.(width);
		}
		return this.getLogicalRowCount(width);
	}

	render(width: number): string[] {
		return this.renderRows(width, 0, this.getLogicalRowCount(width));
	}
}

const MAX_REPORTED_RENDER_ERRORS = 200;
const reportedRenderErrors = new Set<string>();

/**
 * Render a component's lines without letting a thrown error escape the frame.
 *
 * The TUI render loop ({@link TUI.#doRender}) runs inside a `nextTick`/`setTimeout`
 * with no try/catch, and the process installs a fail-fast `uncaughtException`
 * handler that exits. So a single component whose `render()` throws (e.g. a tool
 * renderer fed an optional/undefined field) used to take down the whole app —
 * fatal on whatever happened to trigger the frame (a keystroke, resize, or a
 * command such as `/background`). Isolate the failure: log it once, emit a
 * visible fallback line, and keep rendering the rest of the tree.
 */
function renderFailure(component: Component, where: string, err: unknown): string[] {
	const name = component?.constructor?.name ?? "Component";
	const key = `${where}:${name}:${err instanceof Error ? err.message : String(err)}`;
	if (!reportedRenderErrors.has(key)) {
		if (reportedRenderErrors.size >= MAX_REPORTED_RENDER_ERRORS) reportedRenderErrors.clear();
		reportedRenderErrors.add(key);
		logger.error("Component render failed; emitting fallback line", {
			where,
			component: name,
			error: err instanceof Error ? err.message : String(err),
			stack: err instanceof Error ? err.stack : undefined,
		});
	}
	return [`[render error: ${name}]`];
}

function safeRenderComponent(component: Component, width: number, where: string): string[] {
	try {
		return component.render(width);
	} catch (err) {
		return renderFailure(component, where, err);
	}
}

