/**
 * Resolves a semantic source position to a logical row without materializing a
 * transcript-wide rendered frame. The row index owns eviction/rebuild lookup.
 */
export interface ViewportAnchorResolver {
	resolveViewportAnchor(sourceId: string, graphemeIndex: number, width: number): number | undefined;
}

/**
 * Optional rendering contract for large, row-addressable components.
 *
 * Implementations keep their canonical content and answer only the requested
 * row window. Lines and metadata MUST be generated by the same render pass.
 */
export interface ViewportComponent extends Component, ViewportAnchorResolver {
	/** Explicit opt-in; ordinary row-addressable components use the legacy TUI composition path. */
	readonly isViewportSource: true;
	getLogicalRowCount(width: number): number;
	renderRows(width: number, start: number, end: number): string[];
	renderRowsWithMetadata(width: number, start: number, end: number): ViewportRowWindow;
	getDirtyRanges?(): readonly ViewportDirtyRange[];
	/** Resolve only the tail rows needed to keep the default viewport bottom-anchored after a resize. */
	prepareBottomViewport?(width: number, rows: number): void;
}

/** Optional row-addressable contract for children of a viewport container. */
export interface ViewportRowComponent extends Component {
	getLogicalRowCount(width: number): number;
	renderRows(width: number, start: number, end: number): string[];
	renderRowsWithMetadata?(width: number, start: number, end: number): ViewportRowWindow;
	getViewportSourceIds?(): readonly string[];
	/** Resolve a semantic source to this component's local row coordinate. */
	resolveViewportAnchor?(sourceId: string, graphemeIndex: number, width: number): number | undefined;
	/** Resolve a bounded adapter's actual row count when it enters a visible window. */
	reconcileLogicalRowCount?(width: number): number;
	/** True when `getLogicalRowCount` is independent of width. */
	rowCountIsWidthInvariant?: boolean;
}

export function isViewportRowComponent(component: Component): component is ViewportRowComponent {
	return (
		typeof (component as Partial<ViewportRowComponent>).getLogicalRowCount === "function" &&
		typeof (component as Partial<ViewportRowComponent>).renderRows === "function"
	);
}

let frameAllocationReporter: ((rows: readonly string[]) => void) | undefined;
let frameStringReporter: ((value: string) => void) | undefined;
let frameObjectReporter: ((count: number) => void) | undefined;
let frameAllocationEventObserver: ((event: FrameAllocationEvent) => void) | undefined;

export interface FrameAllocationEvent {
	site: string;
	category: "array" | "object" | "string";
	length: number;
}

export interface BoundedRenderComponent extends Component {
	/** Generate no more than maxRows rows without first materializing full output. */
	renderBounded(width: number, maxRows: number): string[];
}

export function isBoundedRenderComponent(component: Component): component is BoundedRenderComponent {
	return typeof (component as Partial<BoundedRenderComponent>).renderBounded === "function";
}

/** Report a row array at its materialization site during an active TUI frame. */
export function recordFrameAllocationRowArray(rows: readonly string[], site = "row-array"): void {
	frameAllocationEventObserver?.({ site, category: "array", length: rows.length });
	frameAllocationReporter?.(rows);
}

/** Report a frame-local string at its materialization site during an active TUI frame. */
export function recordFrameAllocationString(value: string, site = "string"): void {
	frameAllocationEventObserver?.({ site, category: "string", length: Buffer.byteLength(value) });
	frameStringReporter?.(value);
}

/** Report transient index or metadata objects materialized during an active TUI frame. */
export function recordFrameAllocationObjects(count: number, site = "object"): void {
	const normalizedCount = Math.max(0, count);
	frameAllocationEventObserver?.({ site, category: "object", length: normalizedCount });
	frameObjectReporter?.(normalizedCount);
}

/**
 * Adapts extension-owned render-only content for use inside a viewport tree.
 *
 * Row-addressable extensions bypass this adapter through {@link capViewportRows}.
 * Bounded extensions can implement `renderBounded(width, maxRows)` to generate
 * only the visible cap. Plain render-only components are output-only
 * compatibility: their full output is generated once per width, cached, and
 * then capped. The cap bounds retained/output rows, not generation.
 */
export class CappedViewportRows implements ViewportRowComponent {
	readonly rowCountIsWidthInvariant = true;

	constructor(
		readonly component: Component,
		readonly maxRows: number,
	) {}

	#cachedRows = new Map<number, string[]>();
	#fullRows = new Map<number, string[]>();

	getLogicalRowCount(width: number): number {
		return this.#cachedRows.get(width)?.length ?? this.maxRows;
	}

	renderRows(width: number, start: number, end: number): string[] {
		const rows = this.#rows(width);
		const slice = rows.slice(Math.max(0, start), Math.max(0, end));
		recordFrameAllocationRowArray(slice, "capped-render-rows-slice");
		return slice;
	}

	reconcileLogicalRowCount(width: number): number {
		return this.#rows(width).length;
	}

	renderFull(width: number): string[] {
		return this.#full(width);
	}

	render(width: number): string[] {
		return this.#rows(width);
	}

	#full(width: number): string[] {
		const cached = this.#fullRows.get(width);
		if (cached) return cached;
		// Compatibility-only path: arbitrary Component.render() cannot be bounded.
		const rows = this.component.render(width);
		recordFrameAllocationRowArray(rows, "capped-full-return");
		this.#fullRows.set(width, rows);
		return rows;
	}

	#rows(width: number): string[] {
		const cached = this.#cachedRows.get(width);
		if (cached) return cached;
		const rows = isBoundedRenderComponent(this.component)
			? this.component.renderBounded(width, this.maxRows)
			: this.#full(width).slice(0, this.maxRows);
		recordFrameAllocationRowArray(rows, "capped-output");
		this.#cachedRows.set(width, rows);
		return rows;
	}

	invalidate(): void {
		this.#cachedRows.clear();
		this.#fullRows.clear();
		this.component.invalidate();
	}

	dispose(): void {
		this.component.dispose?.();
	}
}

/**
 * Preserve native row-addressable extensions. Render-only extensions are
 * output-only compatibility and do not receive a generation bound.
 */
export function capViewportRows(component: Component, maxRows: number): ViewportRowComponent {
	if (!Number.isInteger(maxRows) || maxRows < 1) {
		throw new Error("Viewport row cap must be a positive integer");
	}
	return isViewportRowComponent(component) ? component : new CappedViewportRows(component, maxRows);
}

export function isViewportComponent(component: Component): component is ViewportComponent {
	return (component as Partial<ViewportComponent>).isViewportSource === true;
}

